def count_times_in_list(lnks_list):
    link_occurence = {}
    while lnks_list:
        key = lnks_list[0]
        link_occurence[key] = lnks_list.count(key)
        lnks_list_temp = [links for links in lnks_list if links != key]
        lnks_list = lnks_list_temp
    return link_occurence


